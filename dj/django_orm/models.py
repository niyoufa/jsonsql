#coding=utf-8
from django.db import models
class order(models.Model):
    address_id=models.CharField(max_length=255)
    goldbean=models.IntegerField()
    pay_time=models.CharField(max_length=255)
    promotion_id=models.CharField(max_length=255)
    contact_name=models.CharField(max_length=255)
    receive_wx_notify=models.IntegerField()
    pay_type=models.CharField(max_length=255)
    user_id=models.CharField(max_length=255)
    goods_amount=models.FloatField()
    order_status=models.IntegerField()
    pay_status=models.IntegerField()
    money_paid=models.IntegerField()
    order_id=models.CharField(max_length=255)
    origin_code=models.CharField(max_length=255)
    discount=models.IntegerField()
    shipping_status=models.IntegerField()
    order_address=models.TextField()
    order_invoice=models.TextField()
    add_time=models.CharField(max_length=255)
    delivery_time=models.CharField(max_length=255)
    remark=models.CharField(max_length=255)
    shipping_fee=models.IntegerField()
    mobile=models.CharField(max_length=255)
    order_goods=models.TextField()
    edit_times=models.IntegerField()
    _id=models.CharField(max_length=255,db_index=True,unique=True)
